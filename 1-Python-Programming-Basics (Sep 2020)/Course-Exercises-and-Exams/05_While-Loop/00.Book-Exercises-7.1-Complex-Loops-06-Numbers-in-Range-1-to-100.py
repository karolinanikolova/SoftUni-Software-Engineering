# число в диапазона [1 … 100]
# Да се въведе цяло число в диапазона [1 … 100]. Ако то е невалидно, да се въведе отново.
# В случая, за невалидно число ще считаме всяко такова, което не е в зададения диапазон.

number = int(input())

while number < 1 or number > 100:
    print(f'Invalid number!')
    number = int(input())

print(number)