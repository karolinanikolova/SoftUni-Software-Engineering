# квадратна рамка
# Да се напише програма, която въвежда цяло положително число n и чертае на конзолата квадратна рамка с размер N x N.

N = int(input())

for row in range(1, N + 1):
    if row == 1 or row == N:
        print('+ ' + '- ' * (N - 2) + '+')
    else:
        print('| ' + '- ' * (N - 2) + '|')